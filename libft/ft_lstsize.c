#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	size_t count;

	if (lst == NULL)
		return (0);
	count = 1;
	while (lst->next != NULL)
	{
		lst = lst->next;
		count++;
	}
	return (count);
}
