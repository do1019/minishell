#include "../include/minishell.h"

void	convert_lowcase(char *str)
{
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str += 'a' - 'A';
		str++;
	}
}

void	check_upcase(char *s, int i)
{
	if (i != 0)
		return ;
	if (((s[0] == 'e' || s[0] == 'E') && (s[1] == 'c' || s[1] == 'C')
			&& (s[2] == 'h' || s[2] == 'H') && (s[3] == 'o' || s[3] == 'O')
			&& (s[4] == '\0'))
		|| ((s[0] == 'c' || s[0] == 'C') && (s[1] == 'd' || s[1] == 'D')
			&& (s[2] == '\0'))
		|| ((s[0] == 'p' || s[0] == 'P') && (s[1] == 'w' || s[1] == 'W')
			&& (s[2] == 'd' || s[2] == 'D') && (s[3] == '\0'))
		|| ((s[0] == 'e' || s[0] == 'E') && (s[1] == 'x' || s[1] == 'X')
			&& (s[2] == 'p' || s[2] == 'P') && (s[3] == 'o' || s[3] == 'O')
			&& (s[4] == 'r' || s[4] == 'R') && (s[5] == 't' || s[5] == 'T')
			&& (s[6] == '\0'))
		|| ((s[0] == 'u' || s[0] == 'U') && (s[1] == 'n' || s[1] == 'N')
			&& (s[2] == 's' || s[2] == 'S') && (s[3] == 'e' || s[3] == 'E')
			&& (s[4] == 't' || s[4] == 'T') && (s[5] == '\0'))
		|| ((s[0] == 'e' || s[0] == 'E') && (s[1] == 'n' || s[1] == 'N')
			&& (s[2] == 'v' || s[2] == 'V') && (s[3] == '\0'))
		|| ((s[0] == 'e' || s[0] == 'E') && (s[1] == 'x' || s[1] == 'X')
			&& (s[2] == 'i' || s[2] == 'I') && (s[3] == 't' || s[3] == 'T')
			&& (s[4] == '\0')))
		convert_lowcase(s);
}
